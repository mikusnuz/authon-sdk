export function getStrings(locale: string): TranslationStrings {
  if (locale in translations) return translations[locale as AuthonLocale];
  const lang = locale.split('-')[0].split('_')[0];
  if (lang in translations) return translations[lang as AuthonLocale];
  return translations.en;
}
